import type { Skill } from "../loader/schemas.js";
import type { GeneratedFile } from "./opencode/agent-generator.js";

/**
 * True when the team's resolved skill set includes presentation-design,
 * meaning the project will produce HTML presentations that should be published.
 */
export function teamUsesPresentations(skills: Skill[]): boolean {
  return skills.some((s) => s.id === "presentation-design");
}

/**
 * Generate a GitHub Pages workflow that publishes the project's `docs/` folder.
 * If `mkdocs.yml` is present (document mode), runs `mkdocs build` and publishes
 * the `_site/` output. Otherwise publishes `docs/` directly as a static site.
 *
 * Trigger: push to `main`. The user is responsible for merging the
 * `abax/<project>` branch to main when ready to publish.
 *
 * One-time manual setup needed by the user:
 *   1. Push the workflow to main.
 *   2. Repository → Settings → Pages → Source: GitHub Actions.
 *   3. The first push to main runs the workflow and surfaces the URL.
 *
 * Final URL: https://<user>.github.io/<repo>/
 */
export function generatePagesWorkflow(): GeneratedFile {
  const content = `# Auto-generated by Abax Swarm.
# Publishes the project's docs/ folder to GitHub Pages.
# - If mkdocs.yml exists, runs \`mkdocs build\` (modo document) — los .md se
#   procesan a HTML automaticamente con MkDocs Material.
# - Otherwise, publishes docs/ directly as a static site (solo .html y assets,
#   los .md devuelven 404).
#
# Triggered on push to main. Merge your work branch (abax/<project>) to main
# when ready to publish.
#
# One-time setup REQUIRED para que este workflow se ejecute:
#
#   1. Repository -> Settings -> Pages -> Source: GitHub Actions
#      (NO "Deploy from a branch" — ese es modo legacy y bypassea este workflow).
#
#   O via gh CLI:
#      gh api -X PUT repos/<owner>/<repo>/pages \\
#        -f build_type=workflow
#
# Si Pages estaba en modo legacy antes (source: main:/docs), el cambio a
# Actions se aplica desde el SIGUIENTE push. El workflow corre y la URL
# publica final aparece en el log de Actions y en Settings -> Pages.
#
# Verificar que esta en modo Actions:
#   gh api repos/<owner>/<repo>/pages | jq -r '.build_type'
#   # Debe imprimir: workflow
#
# Si quieres que .md se publiquen como HTML pero NO usas modo document,
# agrega un mkdocs.yml minimo manualmente:
#   site_name: Mi Proyecto
#   docs_dir: docs
#   theme:
#     name: material
# Eso activa el modo MkDocs en este workflow y los .md se renderean.

name: Deploy docs to GitHub Pages

on:
  push:
    branches: [main]
  workflow_dispatch:

permissions:
  contents: read
  pages: write
  id-token: write

# Allow one concurrent deployment, queue others (don't cancel mid-deploy).
concurrency:
  group: pages
  cancel-in-progress: false

jobs:
  build:
    runs-on: ubuntu-latest
    steps:
      - uses: actions/checkout@v4

      - name: Configure Pages
        uses: actions/configure-pages@v5

      - name: Detect build mode
        id: mode
        run: |
          if [ -f mkdocs.yml ]; then
            echo "kind=mkdocs" >> "$GITHUB_OUTPUT"
          elif [ -d docs ]; then
            echo "kind=static" >> "$GITHUB_OUTPUT"
          else
            echo "kind=none" >> "$GITHUB_OUTPUT"
          fi

      - name: Setup Python (MkDocs build)
        if: steps.mode.outputs.kind == 'mkdocs'
        uses: actions/setup-python@v5
        with:
          python-version: '3.12'

      - name: Build with MkDocs
        if: steps.mode.outputs.kind == 'mkdocs'
        run: |
          if [ -f requirements.txt ]; then
            pip install -r requirements.txt
          else
            pip install mkdocs-material
          fi
          mkdocs build --site-dir _site

      - name: Use docs/ directly
        if: steps.mode.outputs.kind == 'static'
        run: |
          mkdir -p _site
          cp -R docs/. _site/

      - name: No docs to publish
        if: steps.mode.outputs.kind == 'none'
        run: |
          echo "No mkdocs.yml or docs/ found; nothing to publish."
          mkdir -p _site
          echo "<h1>No docs yet</h1>" > _site/index.html

      - name: Upload artifact
        uses: actions/upload-pages-artifact@v3
        with:
          path: _site

  deploy:
    needs: build
    runs-on: ubuntu-latest
    environment:
      name: github-pages
      url: \${{ steps.deployment.outputs.page_url }}
    steps:
      - name: Deploy
        id: deployment
        uses: actions/deploy-pages@v4
`;
  return {
    path: ".github/workflows/pages.yml",
    content,
  };
}
